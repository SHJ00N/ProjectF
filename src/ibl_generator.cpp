#pragma region loader functions
void loadIBLShaders()
{
    ResourceManager::LoadShader("shaders/IBL_shader/cubemap.vert", "shaders/IBL_shader/equirectangular_to_cubemap.frag", nullptr, nullptr, nullptr, "envCubeMap");
    ResourceManager::LoadShader("shaders/IBL_shader/cubemap.vert", "shaders/IBL_shader/irradiance_convolution.frag", nullptr, nullptr, nullptr, "irradianceMap");
    ResourceManager::LoadShader("shaders/IBL_shader/cubemap.vert", "shaders/IBL_shader/prefilter.frag", nullptr, nullptr, nullptr, "prefilterMap");
}

void loadBrdfShader()
{
    ResourceManager::LoadShader("shaders/IBL_shader/brdf.vert", "shaders/IBL_shader/brdf.frag", nullptr, nullptr, nullptr, "brdfLUT");
}

unsigned int loadFromeTextureFile(const char *path)
{
    stbi_set_flip_vertically_on_load(true);
    unsigned int texture;
    
    int width, height, nrComponents;
    float *data = stbi_loadf(path, &width, &height, &nrComponents, 0);
    if(data)
    {
        glGenTextures(1, &texture);
        glBindTexture(GL_TEXTURE_2D, texture);
        glTexImage2D(GL_TEXTURE_2D, 0, GL_RGB16F, width, height, 0, GL_RGB, GL_FLOAT, data);

        glTexParameteri(GL_TEXTURE_2D, GL_TEXTURE_WRAP_S, GL_CLAMP_TO_EDGE);
        glTexParameteri(GL_TEXTURE_2D, GL_TEXTURE_WRAP_T, GL_CLAMP_TO_EDGE);
        glTexParameteri(GL_TEXTURE_2D, GL_TEXTURE_MIN_FILTER, GL_LINEAR);
        glTexParameteri(GL_TEXTURE_2D, GL_TEXTURE_MAG_FILTER, GL_LINEAR); 
    }
    stbi_image_free(data);
    stbi_set_flip_vertically_on_load(false);
    return texture;
}

